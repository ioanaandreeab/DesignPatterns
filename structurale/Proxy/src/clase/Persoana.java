package clase;

public class Persoana {
	private String nume;
	private boolean areAsigurare;
	
	public Persoana(String nume, boolean areAsigurare) {
		super();
		this.nume = nume;
		this.areAsigurare = areAsigurare;
	}

	public String getNume() {
		return nume;
	}

	public boolean isAreAsigurare() {
		return areAsigurare;
	}
	
}
